import os

import pandas as pd

from datasets.csv_dataset import CSVDataset
from datasets.multi_dataset import MultiDataset

_DESCRIPTION = """
This dataset consists of images generated by running the PerfectAsciiConverter (with the Ubuntu Mono Font: 
https://fonts.google.com/specimen/Ubuntu+Mono) on a series of color images. The source images were preprocessed by first
converting them to high contrast black and white images with the Otsu filter run on each 8x8 block of the image, and 
subsequently fed to the ASCII converter, which matched each of these blocks to the closest ASCII character available 
in the character set.

Each text file is a tab delimited CSV which is the result of running this process over one source image. Each line
in the file represents one training example, or one processed 8x8 binary block of the source image. The first column 
represents the label (ID of the character chosen for this particular block), and columns 2-65 the binary pixel data
of the source image in 0-1 range.
"""

class UbuntuMono(MultiDataset):
    dataset_name = "ubuntu_mono"

    def __init__(self, transform=None, target_transform=None, subdir=None, device=None):
        super().__init__(transform=transform, target_transform=target_transform, subdir=subdir, device=device)
